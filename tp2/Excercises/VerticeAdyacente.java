package tp2.Excercises;

public class VerticeAdyacente {
    public int nodo;
    public int peso;
    
    public VerticeAdyacente(int nodo, int peso) {
        this.nodo = nodo;
        this.peso = peso;
    }

    public int getNodo() {
		return nodo;
	}

	public int getPeso() {
		return peso;
	}
}
